     int conta1=0,conta2=0, conta3=0;
    
    public void prueba(){ 
        if(cola.Mostra1_Vacia()){
            conta1++;
                 int y1 = cola.Extrae();
                 String a1 = String.valueOf(y1);
                 lblCj1.setText(a1);
                 cola.Mostra1_Agrega(y1);
               
        }else{
            if(cola.Mostra2_Vacia()){
                conta2++;
                int y2 = cola.Extrae();
                String a2 = String.valueOf(y2);
                lblCj2.setText(a2);
                cola.Mostra2_Agrega(y2);
              
            }else{
               
                if(cola.Mostra3_Vacia()){
                    conta3++;
                    int y3= cola.Extrae();
                    String a3 = String.valueOf(y3);
                    lblCj3.setText(a3);
                   
                    cola.Mostra3_Agrega(y3);
                }                   
            }
        }
    }
    
